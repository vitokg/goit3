function square(n) {
var block=[];
    for (var i=0; i<block.length; i++) {
        block[i]=document.getElementsByClassName("square");
    }
    for (var i=0; i<block.length; i++) {
        for (var j=0; j<n; j++) {
            block[i].document.getElementsByClassName("square").style.float="left";

        }
    }

}
square();